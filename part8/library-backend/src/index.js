const typeDefs = gql`
  type Book {
    title: String!
    published: Int!
    author: String!
    genres: [String!]!
    id: ID!
  }

  type Author {
    name: String!
    born: Int
    id: ID!
  }

  type AuthorBookCount {
    name: String!
    born: Int
    id: ID!
    bookCount: Int!
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book]
    allAuthors: [AuthorBookCount!]!
  }

  type Mutation {
    addBook(
      title: String!
      published: Int!
      author: String!
      genres: [String!]!
    ): Book
    editAuthor(name: String!, setBornTo: Int!): Author
  }
`

const resolvers = {
  Query: {
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allBooks: (_root, args) => {
      if (args.author)
        return books.reduce(
          (matchedBooks, currentBook) =>
            currentBook.author === args.author
              ? [...matchedBooks, currentBook]
              : matchedBooks,
          []
        )
      if (args.genre)
        return books.reduce(
          (matchedBooks, currentBook) =>
            currentBook.genres.includes(args.genre)
              ? [...matchedBooks, currentBook]
              : matchedBooks,
          []
        )
      return books
    },
    allAuthors: () =>
      authors.map((author) => {
        return {
          name: author.name,
          id: author.id,
          born: author.born,
          bookCount: books.reduce(
            (count, book) => (book.author === author.name ? count + 1 : count),
            0
          ),
        }
      }),
  },

  Mutation: {
    addBook: (_root, args) => {
      if (!authors.map((author) => author.name).includes(args.author)) {
        const author = {
          name: args.author,
          born: null,
          id: uuid(),
        }
        authors = [...authors, author]
      }
      if (books.map((book) => book.title).includes(args.title)) return
      const book = { ...args, id: uuid() }
      books = [...books, book]
      return book
    },
    editAuthor: (_root, args) => {
      const foundAuthor = authors.find((author) => author.name === args.name)
      if (!foundAuthor) return
      const updatedAuthor = { ...foundAuthor, born: args.setBornTo }
      authors = authors.map((author) =>
        author.name === args.name ? updatedAuthor : author
      )
      return updatedAuthor
    },
  },
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
